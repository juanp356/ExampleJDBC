    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        try {
            Key key = new Key();
            key.setId(Integer.parseInt(jTextFieldID.getText()));
            key.setName(jTextFieldName.getText().toUpperCase());
            key.setRoom(jTextFieldRoom.getText());
            key.setCount(Integer.parseInt(jTextFieldCount.getText()));
            key.setObservation(jTextAreaObservation.getText());
            keyController.insert(key);
            MessageUtils.showInfoMessage("llave creada exitosamente");
            fillTable();
        } catch (Exception e) {
            MessageUtils.showErrorMessage(e.getMessage());
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        try {
            int option = JOptionPane.showConfirmDialog(rootPane, 
                    "esta seguro de eliminar el registro","confirmacion",JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){
                keyController.delete(Integer.parseInt(jTextFieldID.getText()));
                MessageUtils.showInfoMessage("Llave eliminada exitosamente");
                fillTable();
            }
            clean();
        } catch (Exception e) {
            MessageUtils.showErrorMessage(e.getMessage());
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jTableKeysMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableKeysMouseClicked
        int rowSelected = jTableKeys.getSelectedRow();
        if(rowSelected != 1){
            
            
            int idSelected = Integer.parseInt(jTableKeys.getValueAt(rowSelected, 0).toString());
            
            try {
                Key key = keyController.findById(idSelected);
                jTextFieldID.setText(String.valueOf(idSelected));
                jTextFieldName.setText(key.getName());
                jTextFieldRoom.setText(key.getRoom());
                jTextFieldCount.setText(String.valueOf(key.getCount()));
                jTextAreaObservation.setText(key.getObservation());

                jButtonCreate.setEnabled(false);
                jButtonUpdate.setEnabled(true);
                jButtonDelete.setEnabled(true);
            } catch (Exception e) {
                MessageUtils.showErrorMessage(e.getMessage());
            }
            
            
          
            
        }
    }//GEN-LAST:event_jTableKeysMouseClicked

